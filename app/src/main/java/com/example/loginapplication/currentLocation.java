public class currentLocation extends FragmentActivity implements OnMapReadyCallback {

    private GoogleMap mMap;
    private Switch currentLocationSwitch;

    @SuppressLint("WrongViewCast")
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_maps);
        currentLocationSwitch = findViewById(R.id.currentLocationSwitch);

        currentLocationSwitch.setOnClickListener(new View.OnClickListener()
        {
            @Override
            public void onClick(View view)
            {
                changeActivity();
            }
        });
        currentLocationSwitch.setChecked(true);
        // Obtain the SupportMapFragment and get notified when the map is ready to be used.
        SupportMapFragment mapFragment = (SupportMapFragment) getSupportFragmentManager()
                .findFragmentById(R.id.map);
        mapFragment.getMapAsync(this);
    }


    private void changeActivity()
    {
        Intent intent = new Intent(this, MapsActivity.class);
        startActivity(intent);

    }

    @Override
    public void onMapReady(GoogleMap googleMap) {
        mMap = googleMap;


        // Add a marker in New Brunswick (40.4862N, 74.4518W) and move the camera
        LatLng newBrunswick = new LatLng(40.5187,-74.4121);
        mMap.addMarker(new MarkerOptions().position(newBrunswick).title("Marker in Edison"));
        mMap.moveCamera(CameraUpdateFactory.newLatLng(newBrunswick));
        mMap.setMinZoomPreference(15);
    }
}

